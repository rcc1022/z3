DROP TABLE IF EXISTS t_football;
CREATE TABLE t_football
(
  c_id CHAR(36) NOT NULL COMMENT '主键',
  c_group INT DEFAULT 0 COMMENT '组',
  c_gid VARCHAR(255) NOT NULL COMMENT '比赛ID',
  c_sort INT DEFAULT 0 COMMENT '排序',
  c_league VARCHAR(255) DEFAULT NULL COMMENT '联赛',
  c_team_h VARCHAR(255) DEFAULT NULL COMMENT '主队名',
  c_team_c VARCHAR(255) DEFAULT NULL COMMENT '客队名',
  c_score_h INT DEFAULT 0 COMMENT '主队得分',
  c_score_c INT DEFAULT 0 COMMENT '客队得分',
  c_score_shang_h INT DEFAULT 0 COMMENT '主队上半场得分',
  c_score_shang_c INT DEFAULT 0 COMMENT '客队上半场得分',
  c_strong VARCHAR(255) DEFAULT NULL COMMENT '强队',
  c_rang_qiu VARCHAR(255) DEFAULT NULL COMMENT '让球',
  c_rang_qiu_h VARCHAR(255) DEFAULT NULL COMMENT '主队让球',
  c_rang_qiu_c VARCHAR(255) DEFAULT NULL COMMENT '客队让球',
  c_rang_qiu_shang VARCHAR(255) DEFAULT NULL COMMENT '上半场让球',
  c_rang_qiu_shang_h VARCHAR(255) DEFAULT NULL COMMENT '上半场主队让球',
  c_rang_qiu_shang_c VARCHAR(255) DEFAULT NULL COMMENT '上半场客队让球',
  c_rang_qiu_shang_he VARCHAR(255) DEFAULT NULL COMMENT '上半场让球和',
  c_de_fen_h VARCHAR(255) DEFAULT NULL COMMENT '主队得分',
  c_de_fen_c VARCHAR(255) DEFAULT NULL COMMENT '客队得分',
  c_de_fen_rate_h VARCHAR(255) DEFAULT NULL COMMENT '主队得分赔率',
  c_de_fen_rate_c VARCHAR(255) DEFAULT NULL COMMENT '客队得分赔率',
  c_de_fen_shang_h VARCHAR(255) DEFAULT NULL COMMENT '主队上半场得分',
  c_de_fen_shang_c VARCHAR(255) DEFAULT NULL COMMENT '客队上半场得分',
  c_de_fen_rate_shang_h VARCHAR(255) DEFAULT NULL COMMENT '主队上半场得分赔率',
  c_de_fen_rate_shang_c VARCHAR(255) DEFAULT NULL COMMENT '客队上半场得分赔率',
  c_du_ying VARCHAR(255) DEFAULT NULL COMMENT '独赢',
  c_du_ying_h VARCHAR(255) DEFAULT NULL COMMENT '主队独赢',
  c_du_ying_c VARCHAR(255) DEFAULT NULL COMMENT '客队独赢',
  c_du_ying_he VARCHAR(255) DEFAULT NULL COMMENT '独赢和',
  c_du_ying_shang VARCHAR(255) DEFAULT NULL COMMENT '上半场独赢',
  c_du_ying_shang_h VARCHAR(255) DEFAULT NULL COMMENT '上半场主队独赢',
  c_du_ying_shang_c VARCHAR(255) DEFAULT NULL COMMENT '上半场客队独赢',
  c_du_ying_shang_he VARCHAR(255) DEFAULT NULL COMMENT '上半场独赢和',
  c_bo_dan TEXT DEFAULT NULL COMMENT '波胆',
  c_datetime VARCHAR(255) DEFAULT NULL COMMENT '时间',
  c_timer VARCHAR(255) DEFAULT NULL COMMENT '计时器',
  c_on INT DEFAULT 0 COMMENT '开启：0-否；1-是',
  c_timestamp BIGINT DEFAULT 0 COMMENT '更新时间戳',
  c_zero INT DEFAULT 0 COMMENT '空数据：0-否；1-是',

  PRIMARY KEY pk(c_id) USING HASH,
  UNIQUE KEY uk_group_gid(c_group,c_gid) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;